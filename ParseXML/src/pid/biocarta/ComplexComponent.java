package pid.biocarta;

public class ComplexComponent {
private String complexComponentID;
private String labelType;
private String labelValue;

public String getLabelType() {
	return labelType;
}

public void setLabelType(String labelType) {
	this.labelType = labelType;
}

public String getLabelValue() {
	return labelValue;
}

public void setLabelValue(String labelValue) {
	this.labelValue = labelValue;
}

public String getComplexComponentID() {
	return complexComponentID;
}

public void setComplexComponentID(String complexComponentID) {
	this.complexComponentID = complexComponentID;
} 
}
